function getCommandDesc(cmd: string): string {
  return COMMAND_DESCS[cmd] || cmd;
}

function getVerbDesc(cmd: string, verb: string): string {
  return VERB_DESCS[cmd]?.[verb] || verb;
}

/* ------------------------------------------------------------------ */
/*  Generators map                                                     */
/* ------------------------------------------------------------------ */

const GENERATORS: Record<string, () => string> = {
  bash: generateBash,
  zsh: generateZsh,
  fish: generateFish,
};

/* ------------------------------------------------------------------ */
/*  Router                                                             */
/* ------------------------------------------------------------------ */

export async function run(args: string[], opts: GlobalOptions): Promise<void> {
  const verb = args[0] || '';

  if (!verb || verb === 'help' || verb === '--help' || verb === '-h') {
    printSubcommandHelp('completion', VERBS);
    return;
  }

  const known = VERBS.find((v) => v.name === verb);
  if (!known) {
    writeError(`Unknown completion target: ${verb}`);
    printSubcommandHelp('completion', VERBS);
    process.exitCode = 1;
    return;
  }

  const generator = GENERATORS[verb];
  const script = generator();

  if (opts.json) {
    process.stdout.write(
      JSON.stringify({ shell: verb, script }) + '\n'
    );
  } else {
    process.stdout.write(script);
  }
}
